int		ft_strcmp(char *s1, char *s2)
{
	int i;
	int result;

	i = 0;
	result = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
		{
			result += (s1[i] - s2[i]);
			break ;
		}
		i++;
	}
	return (result);
}
